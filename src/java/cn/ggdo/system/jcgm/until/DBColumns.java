package cn.ggdo.system.jcgm.until;

public class DBColumns {
	private String columnsName;
	private String columnsType;
	private String columnsLength;
	private String columnsComment;
	private String columnsNullAble;
	
	public String getColumnsNullAble() {
		return columnsNullAble;
	}

	public void setColumnsNullAble(String columnsNullAble) {
		this.columnsNullAble = columnsNullAble;
	}

	public String getColumnsName() {
		return columnsName;
	}

	public void setColumnsName(String columnsName) {
		this.columnsName = columnsName;
	}

	public String getColumnsType() {
		return columnsType;
	}

	public void setColumnsType(String columnsType) {
		this.columnsType = columnsType;
	}

	public String getColumnsLength() {
		return columnsLength;
	}

	public void setColumnsLength(String columnsLength) {
		this.columnsLength = columnsLength;
	}

	public String getColumnsComment() {
		return columnsComment;
	}

	public void setColumnsComment(String columnsComment) {
		this.columnsComment = columnsComment;
	}

}
